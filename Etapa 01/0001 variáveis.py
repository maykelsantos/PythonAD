# Variáveis de texto

varTexto1 = 'Olá, mundo!'
varTexto2 = str('Olá, mundo 1!')

print(varTexto1, varTexto2)

# Variáveis de números

varInteiro1 = 100
varInteiro2 = int(200)

print(varInteiro1, varInteiro2)

# Variáveis flutuantes

varFlutuante1 = 10.9
varFlutuante2 = float(10.5)

print(varFlutuante1, varFlutuante2)

# Variáveis booleanos

varBooleano1 = True
varBooleano2 = False

print(varBooleano1, varBooleano2)